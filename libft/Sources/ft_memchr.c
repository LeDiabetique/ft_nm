#include "libft.h"

void	*ft_memchr(const void	*s, int c, size_t n)
{
	unsigned const char	*ps;

	ps = (unsigned const char *)s;
	while (n)
	{
		if (*ps == (unsigned char)c)
			return ((unsigned char *)ps);
		ps++;
		n--;
	}
	return (NULL);
}
